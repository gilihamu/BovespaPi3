    public void novo() {
        this.txtRzsocial.setText(null);
        this.txtCnpj.setText(null);
        this.txtRzsocial.setEnabled(true);
        this.jTable1.setEnabled(false);
        this.editar.setEnabled(false);
        this.btExcluir.setEnabled(false);
    }

    private void salva() {
        if (this.txtRzsocial.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Campo Razão Social deverá ser preenchido.");
        }
        if (this.txtCnpj.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Campo CNPJ deverá ser preenchido.");
        }
        Empresa empresa = new Empresa();

        empresa.setCnpj(this.txtCnpj.getText());
        empresa.setRazaoSocial(this.txtRzsocial.getText());

        empDao.persist(empresa);
        JOptionPane.showMessageDialog(null, "Empresa cadastrada com Sucesso!");
        initTela();

    }

