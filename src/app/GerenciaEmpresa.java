    private void limpaCampo() {
        this.txtCnpj.setText(null);
        this.txtRzsocial.setText(null);
    }

    public void tableModel() {
        this.jTable1.getColumnModel().getColumn(0).setPreferredWidth(25);
        this.jTable1.getColumnModel().getColumn(1).setPreferredWidth(100);
        this.jTable1.getColumnModel().getColumn(2).setPreferredWidth(200);

        model = (DefaultTableModel) this.jTable1.getModel();
        model.setNumRows(0);
    }

    public void cargarTabla() {

        List<Empresa> lista = (List<Empresa>) empDao.findAll();
        for (Empresa empr : lista) {
            String cnpj = empr.getCnpj();
            model.addRow(new Object[]{
                empr.getId(), empr.getCnpj(), empr.getRazaoSocial()
            });
        }
    }

    private void initTela() {
        tableModel();
        cargarTabla();
        limpaCampo();
    }

    private void excluir() {

        int showConfirmDialog = JOptionPane.showConfirmDialog(null, "Confirma a exclusão do Registro ?", " Exclusão da Empresa", JOptionPane.YES_NO_OPTION);
        if (showConfirmDialog == 1) {
            JOptionPane.showMessageDialog(null, "Registro não excluido!");
        } else {
            int selectedRow = this.jTable1.getSelectedRow();
            Object valueAt = model.getValueAt(selectedRow, 0);
            Integer idArt = Integer.parseInt(valueAt.toString());
            Empresa empr = empDao.getById(idArt);
            if (empr == null) {
                JOptionPane.showMessageDialog(null, "Registro excluido por outra tela");
            }
            empDao.remove(empr);
            JOptionPane.showMessageDialog(null, "Registro excluído com sucesso!");
            initTela();
        }
    }

    public void obterEmpresa() {
        if (this.jTable1.isEnabled()) {
            int selectedRow = this.jTable1.getSelectedRow();
            int idFunc = Integer.parseInt(String.valueOf(model.getValueAt(selectedRow, 0)));
            Empresa empr = (Empresa) empDao.getById(idFunc);
            this.txtCnpj.setText(empr.getCnpj());
            this.txtRzsocial.setText(empr.getRazaoSocial());
            this.editar.setEnabled(true);
            this.btExcluir.setEnabled(true);
        }
    }

